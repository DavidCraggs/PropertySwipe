/**
 * Helper function to get rental properties by city
 */
export const getPropertiesByCity = (city: string): Property[] => {
  return mockProperties.filter((property) => property.address.city === city);
};

/**
 * Helper function to get rental properties within rent range
 */
export const getPropertiesByRentRange = (min: number, max: number): Property[] => {
  return mockProperties.filter((property) => property.rentPcm >= min && property.rentPcm <= max);
};

/**
 * Helper function to filter rental properties by bedrooms
 */
export const getPropertiesByBedrooms = (bedrooms: number): Property[] => {
  return mockProperties.filter((property) => property.bedrooms === bedrooms);
};
